<div class="description boom-swinger-description" id="boom-swinger">

    <div class="container">
        <div class="row">
            <div class="text-wrapper">
                <h3 class="section-title"><span>Boom-Swinger</span></h3>
            </div>
            <div class="col-sm-12 col-md-4">
                <h4 class="text-header">Product Description</h4>
                <p>No more aching arms, the Boom-Swinger takes the weight of the mic boom whilst still allowing
                    the
                    operator to move in and out of the action and rotate the mic even whilst using the
                    mixer.</p>

                <p>By using another mic boom as the support, the Boom-Swinger is an invaluable aid for those
                    long
                    interviews and drama sequences.</p>

                <p>Designed for use with booms of 19mm diameter or more at the point of pivot. The main boom is
                    supported between two precision ground nitrile spheres which rotate as the boom is
                    twisted.</p>
            </div>

            <div class="col-sm-12 col-md-4">
